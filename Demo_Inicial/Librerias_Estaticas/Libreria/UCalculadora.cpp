int sumar(const int x, const int y)
{
	return x + y;
}
int restar(const int x, const int y)
{
	return x - y;
}
int multiplicar(const int x, const int y)
{
	return x * y;
}
int dividir(const int x, const int y)
{
    return x / y;
}
